    private void actualizarTabla(){
        
        String [] encabezado = new String[4]; 
        
        encabezado[0]="Nombre"; 
        encabezado[1]="Edad"; 
        encabezado[2]="Cedula";
        encabezado[3]="Pais";
        
        var datos = new Object[this.jugadorControl.listar().size()][4];
        var i=0; 
        for(var jugador: this.jugadorControl.listar()){
            
            datos[i][0]=jugador.getNombre(); 
            datos[i][1]=jugador.getEdad(); 
            datos[i][2]=jugador.getNumeroCedula(); 
            datos[i][3]=jugador.getNacionalidad();

            i++; 
        }
        
        this.modeloTabla = new DefaultTableModel(datos, encabezado); 
        this.jTable1.setModel(modeloTabla);
    }
    
